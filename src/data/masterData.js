// Use this wherever you need the zone dropdown.
export const zones = ["North", "South", "East", "West"];

// Get states belonging to a selected zone.
export const getStatesByZone = (selectedZone) => {
  if (!selectedZone) return [];

  return locations
    .filter((location) => location.zone === selectedZone)
    .map((location) => location.state)
    .sort((a, b) => a.localeCompare(b));
};

// Get cities belonging to a selected zone and state.
export const getCitiesByState = (selectedZone, selectedState) => {
  if (!selectedZone || !selectedState) return [];

  const location = locations.find(
    (item) =>
      item.zone === selectedZone &&
      item.state === selectedState
  );

  return location ? [...location.cities].sort((a, b) => a.localeCompare(b)) : [];
};